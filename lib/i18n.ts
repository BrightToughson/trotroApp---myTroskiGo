const LANGUAGE_KEY = 'user-language';

// Detect device language synchronously
let deviceLanguage = 'en';
try {
  const locales = Localization.getLocales();
  if (locales && locales.length > 0 && locales[0]?.languageCode) {
    deviceLanguage = locales[0].languageCode;
  }
} catch (error) {
  console.log('Error getting device locales:', error);
}

i18n
  .use(initReactI18next)
  .init({
    compatibilityJSON: 'v3',
    resources,
    lng: deviceLanguage,
    fallbackLng: 'en',
    interpolation: {
      escapeValue: false,
    },
    react: {
      useSuspense: false,
    },
  });

// Load saved language asynchronously
AsyncStorage.getItem(LANGUAGE_KEY).then((savedLanguage) => {
  if (savedLanguage && savedLanguage !== deviceLanguage) {
    i18n.changeLanguage(savedLanguage);
  }
}).catch((error) => {
  console.log('Error loading saved language:', error);
});

// Save language changes
i18n.on('languageChanged', (lng) => {
  AsyncStorage.setItem(LANGUAGE_KEY, lng).catch((error) => {
    console.log('Error saving language:', error);
  });
});

export default i18n;
